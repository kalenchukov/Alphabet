package dev.kalenchukov.alphabet;

import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.jetbrains.annotations.Unmodifiable;

import java.util.List;
import java.util.Objects;

/**
 * Класс букв алфавита тагбанва.
 *
 * @author Aleksey Kalenchukov
 */
public final class TagbanwaAlphabet extends AbstractAlphabet implements AlphabeticalCase
{
	/**
	 * Коды букв в Unicode.
	 */
	@Unmodifiable
	@NotNull
	public static final List<@NotNull Character> LETTERS = List.of(
			'\u1760', '\u1761', '\u1762', '\u1763', '\u1764', '\u1765', 
			'\u1766', '\u1767', '\u1768', '\u1769', '\u176A', '\u176B', 
			'\u176C', '\u176E', '\u176F', '\u1770', '\u1772', '\u1773'
	);

	/**
	 * Конструктор для {@code TagbanwaAlphabet}.
	 */
	public TagbanwaAlphabet()
	{
		super(TagbanwaAlphabet.LETTERS);
	}

	/**
	 * {@inheritDoc}
	 *
	 * @param obj {@inheritDoc}
	 * @return {@inheritDoc}
	 */
	@Override
	public boolean equals(@Nullable final Object obj)
	{
		if (obj == null) {
			return false;
		}

		if (this == obj) {
			return true;
		}

		if (this.getClass() != obj.getClass()) {
			return false;
		}

		final TagbanwaAlphabet alphabet = (TagbanwaAlphabet) obj;

		if (!Objects.equals(this.get(), alphabet.get())) {
			return false;
		}

		return true;
	}

	/**
	 * {@inheritDoc}
	 *
	 * @return {@inheritDoc}
	 */
	@Override
	public int hashCode()
	{
		return this.get().hashCode();
	}
}
