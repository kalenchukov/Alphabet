package dev.kalenchukov.alphabet;

import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.jetbrains.annotations.Unmodifiable;

import java.util.List;
import java.util.Objects;

/**
 * Класс букв огамического алфавита.
 *
 * @author Aleksey Kalenchukov
 */
public final class OghamAlphabet extends AbstractAlphabet implements AlphabeticalCase
{
	/**
	 * Коды букв в Unicode.
	 */
	@Unmodifiable
	@NotNull
	public static final List<@NotNull Character> LETTERS = List.of(
			'\u1680', '\u1681', '\u1682', '\u1683', '\u1684', '\u1685', 
			'\u1686', '\u1687', '\u1688', '\u1689', '\u168A', '\u168B', 
			'\u168C', '\u168D', '\u168E', '\u168F', '\u1690', '\u1691', 
			'\u1692', '\u1693', '\u1694', '\u1695', '\u1696', '\u1697', 
			'\u1698', '\u1699', '\u169A', '\u169B', '\u169C'
	);

	/**
	 * Конструктор для {@code OghamAlphabet}.
	 */
	public OghamAlphabet()
	{
		super(OghamAlphabet.LETTERS);
	}

	/**
	 * {@inheritDoc}
	 *
	 * @param obj {@inheritDoc}
	 * @return {@inheritDoc}
	 */
	@Override
	public boolean equals(@Nullable final Object obj)
	{
		if (obj == null) {
			return false;
		}

		if (this == obj) {
			return true;
		}

		if (this.getClass() != obj.getClass()) {
			return false;
		}

		final OghamAlphabet alphabet = (OghamAlphabet) obj;

		if (!Objects.equals(this.get(), alphabet.get())) {
			return false;
		}

		return true;
	}

	/**
	 * {@inheritDoc}
	 *
	 * @return {@inheritDoc}
	 */
	@Override
	public int hashCode()
	{
		return this.get().hashCode();
	}
}
