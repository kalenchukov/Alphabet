package dev.kalenchukov.alphabet;

import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.jetbrains.annotations.Unmodifiable;

import java.util.List;
import java.util.Objects;

/**
 * Класс букв алфавита хануноо.
 *
 * @author Aleksey Kalenchukov
 */
public class HanunooAlphabet extends AbstractAlphabet implements AlphabeticalCase
{
	/**
	 * Коды букв в Unicode.
	 */
	@Unmodifiable
	@NotNull
	public static final List<@NotNull Character> LETTERS = List.of(
			'\u1720', '\u1721', '\u1722', '\u1723', '\u1724', '\u1725', 
			'\u1726', '\u1727', '\u1728', '\u1729', '\u172A', '\u172B', 
			'\u172C', '\u172D', '\u172E', '\u172F', '\u1730', '\u1731', 
			'\u1732', '\u1733', '\u1734', '\u1735', '\u1736'
	);

	/**
	 * Конструктор для {@code HanunooAlphabet}.
	 */
	public HanunooAlphabet()
	{
		super(HanunooAlphabet.LETTERS);
	}

	/**
	 * {@inheritDoc}
	 *
	 * @param obj {@inheritDoc}
	 * @return {@inheritDoc}
	 */
	@Override
	public boolean equals(@Nullable final Object obj)
	{
		if (obj == null) {
			return false;
		}

		if (this == obj) {
			return true;
		}

		if (this.getClass() != obj.getClass()) {
			return false;
		}

		final HanunooAlphabet alphabet = (HanunooAlphabet) obj;

		if (!Objects.equals(this.get(), alphabet.get())) {
			return false;
		}

		return true;
	}

	/**
	 * {@inheritDoc}
	 *
	 * @return {@inheritDoc}
	 */
	@Override
	public int hashCode()
	{
		return this.get().hashCode();
	}
}
